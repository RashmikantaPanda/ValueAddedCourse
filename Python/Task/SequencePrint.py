print("Print orderly - (1,2,3....10)")
#print 1,2,3,4....10
for i in range(1,11):
    print(i,end=" ")

print("\nReverse Order (10,9,8...1)")
#print 10,9,8,7.....1
for j in range(10,0,-1):
    print(j,end=" ")

print("\nOdd Number (1,3,5,...9)")
#print 1,3,5...9
for j in range(1,11,2):
    print(j,end=" ")

print("\nNegative to Positive (9,7,5....-7,-9")
for i in range(9,-9,-2):
    print(i,end=" ")


    